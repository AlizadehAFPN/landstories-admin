// ─── Validation ───
function validate(item, label) {
  let totalChars = 0;
  let pass = true;
  console.log(`\n── ${label} ──`);
  for (let i = 0; i < item.paragraphs.length; i++) {
    const t = item.paragraphs[i].text;
    const chars = t.length;
    const words = t.split(/\s+/).length;
    totalChars += chars;
    const cOk = chars <= 500 ? "✅" : "⚠️";
    const wOk = words <= 100 ? "✅" : "⚠️";
    console.log(`  P${i + 1}: ${chars} chars ${cOk}  ${words} words ${wOk}`);
    if (chars > 600 || words > 120) pass = false; // hard fail with tolerance
  }
  console.log(`  Total: ${totalChars} chars, ${item.paragraphs.length} paragraphs`);
  console.log(`  Target: ~3000 chars (±20% = 2400–3600)`);
  if (totalChars < 2000 || totalChars > 4200) {
    console.error(`  ❌ Total chars out of acceptable range!`);
    pass = false;
  }
  return pass;
}

// ─── Push ───
async function push(item, label) {
  const cmd = new PutCommand({ TableName: TABLE, Item: item });
  const result = await docClient.send(cmd);
  console.log(`\n✅ ${label} pushed successfully!`);
  console.log(`   siteId: ${item.siteId}`);
  console.log(`   langStoryId: ${item.langStoryId}`);
  console.log(`   title: ${item.title}`);
  console.log(`   paragraphs: ${item.paragraphs.length}`);
  console.log(`   updatedAt: ${item.updatedAt}`);
}

async function main() {
  const arOk = validate(ar, "Arabic (ar)");
  const faOk = validate(fa, "Persian (fa)");

  if (!arOk || !faOk) {
    console.error("\n❌ Validation failed. Aborting.");
    process.exit(1);
  }

  await push(ar, "Arabic (ar)");
  await push(fa, "Persian (fa)");

  console.log("\n🎉 All done!");
}

main().catch((err) => {
  console.error("❌ Error:", err.message);
  process.exit(1);
});
