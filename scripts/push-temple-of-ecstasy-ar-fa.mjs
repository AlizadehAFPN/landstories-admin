// ─── Push ───
async function push(record, label) {
  try {
    await docClient.send(
      new PutCommand({ TableName: "Story", Item: record })
    );
    console.log(`✅  ${label} pushed successfully  (langStoryId: ${record.langStoryId})`);
  } catch (err) {
    console.error(`❌  ${label} FAILED:`, err.message);
    process.exit(1);
  }
}

