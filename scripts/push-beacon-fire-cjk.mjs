// PUSH TO DYNAMODB
// ══════════════════════════════════════════════════════

const items = [
  { label: "Chinese (zh)", item: chinese },
  { label: "Japanese (ja)", item: japanese },
  { label: "Korean (ko)", item: korean },
];

for (const { label, item } of items) {
  console.log(`\nPushing ${label}...`);
  try {
    const cmd = new PutItemCommand({
      TableName: "Story",
      Item: item,
      ConditionExpression:
        "attribute_not_exists(siteId) AND attribute_not_exists(langStoryId)",
    });
    const result = await client.send(cmd);
    console.log(
      `  OK — ${label} pushed. HTTP ${result.$metadata.httpStatusCode}`
    );
  } catch (err) {
    if (err.name === "ConditionalCheckFailedException") {
      console.log(`  ${label} already exists — overwriting...`);
      const cmd2 = new PutItemCommand({ TableName: "Story", Item: item });
      const result2 = await client.send(cmd2);
      console.log(
        `  OK — ${label} overwritten. HTTP ${result2.$metadata.httpStatusCode}`
      );
    } else {
      console.error(`  FAILED ${label}:`, err.message);
      process.exit(1);
    }
  }
}

console.log("\nAll three CJK versions pushed successfully.");
console.log(`Timestamp: ${TIMESTAMP}`);
