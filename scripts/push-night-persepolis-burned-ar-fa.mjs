// ─── Push ────────────────────────────────────────────────────────────

async function pushItem(item) {
  const label = `${item.lang} — "${item.title}"`;
  try {
    await docClient.send(
      new PutCommand({
        TableName: TABLE,
        Item: item,
        ConditionExpression:
          "attribute_not_exists(siteId) OR attribute_not_exists(langStoryId)",
      })
    );
    console.log(`✅  ${label} — pushed successfully.`);
    return true;
  } catch (err) {
    if (err.name === "ConditionalCheckFailedException") {
      console.log(`⚠️  ${label} — record exists, overwriting…`);
      await docClient.send(
        new PutCommand({ TableName: TABLE, Item: item })
      );
      console.log(`✅  ${label} — overwritten successfully.`);
      return true;
    }
    console.error(`❌  ${label} — FAILED:`, err.message);
    return false;
  }
}

async function main() {
  console.log(
    `\nTimestamp: ${NOW} (${new Date(NOW * 1000).toISOString()})\n`
  );

  // Validate JSON structure before pushing
  for (const item of [ar, fa]) {
    const label = `${item.lang} — "${item.title}"`;
    if (!item.paragraphs || item.paragraphs.length < 6) {
      console.error(`❌  ${label} — too few paragraphs (${item.paragraphs?.length})`);
      process.exit(1);
    }
    for (let i = 0; i < item.paragraphs.length; i++) {
      const p = item.paragraphs[i];
      if (!p.text || p.text.length === 0) {
        console.error(`❌  ${label} — paragraph ${i} is empty`);
        process.exit(1);
      }
      if (p.text.length > 600) {
        console.warn(`⚠️  ${label} — paragraph ${i} is ${p.text.length} chars (max ~500)`);
      }
    }
    console.log(`✔  ${label} — ${item.paragraphs.length} paragraphs validated`);
  }

  console.log("");

  for (const item of [ar, fa]) {
    const ok = await pushItem(item);
    if (!ok) process.exit(1);
  }

  console.log("\n🏁  Both language versions pushed.\n");
}

main();
