async function push(item, label) {
  console.log(`\n⏳ Pushing ${label}...`);

  // Validate JSON structure
  const json = JSON.stringify(item);
  try {
    JSON.parse(json);
  } catch (e) {
    console.error(`❌ JSON validation failed for ${label}:`, e.message);
    process.exit(1);
  }
  console.log(`  ✓ JSON valid (${json.length} bytes)`);

  // Validate paragraph count
  const paraCount = item.paragraphs.L.length;
  console.log(`  ✓ Paragraphs: ${paraCount}`);
  if (paraCount < 6 || paraCount > 10) {
    console.error(`❌ Paragraph count ${paraCount} out of range [6-10]`);
    process.exit(1);
  }

  // Validate character counts per paragraph
  for (let i = 0; i < paraCount; i++) {
    const text = item.paragraphs.L[i].M.text.S;
    const charCount = text.length;
    const wordCount = text.split(/\s+/).length;
    console.log(`  ¶${i + 1}: ${charCount} chars, ~${wordCount} words`);
    if (charCount > 600) {
      console.warn(`  ⚠️  Paragraph ${i + 1} is ${charCount} chars (max 500 recommended)`);
    }
  }

  // Validate total character count
  const totalChars = item.paragraphs.L.reduce(
    (sum, p) => sum + p.M.text.S.length,
    0
  );
  console.log(`  ✓ Total text: ${totalChars} chars`);

  // Push to DynamoDB
  const cmd = new PutItemCommand({
    TableName: TABLE,
    Item: item,
    ConditionExpression:
      "attribute_not_exists(siteId) AND attribute_not_exists(langStoryId)",
  });

  try {
    await client.send(cmd);
    console.log(`✅ ${label} pushed successfully!`);
  } catch (err) {
    if (err.name === "ConditionalCheckFailedException") {
      console.error(`❌ ${label} already exists! Skipping to avoid overwrite.`);
    } else {
      console.error(`❌ ${label} push failed:`, err.message);
      process.exit(1);
    }
  }
}

