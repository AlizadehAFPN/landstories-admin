// ─── Push helper ──────────────────────────────────────────────────────────────
async function push(label, item) {
  console.log(`\n${"═".repeat(60)}`);
  console.log(`  Pushing ${label}...`);
  console.log(`  siteId:      ${item.siteId.S}`);
  console.log(`  langStoryId: ${item.langStoryId.S}`);
  console.log(`  paragraphs:  ${item.paragraphs.L.length}`);
  console.log(`${"═".repeat(60)}`);

  // Validate JSON serialization before push
  try {
    const serialized = JSON.stringify(item);
    JSON.parse(serialized); // round-trip validation
    console.log(`  JSON validation: OK (${serialized.length} bytes)`);
  } catch (err) {
    console.error(`  JSON validation FAILED: ${err.message}`);
    process.exit(1);
  }

  const cmd = new PutItemCommand({
    TableName: TABLE,
    Item: item,
    ConditionExpression:
      "attribute_not_exists(siteId) AND attribute_not_exists(langStoryId)",
  });

  try {
    const result = await client.send(cmd);
    console.log(
      `  ✓ SUCCESS — HTTP ${result.$metadata.httpStatusCode}`
    );
    return true;
  } catch (err) {
    if (err.name === "ConditionalCheckFailedException") {
      console.error(`  ✗ ALREADY EXISTS — skipping`);
      return false;
    }
    console.error(`  ✗ FAILED: ${err.message}`);
    process.exit(1);
  }
}

