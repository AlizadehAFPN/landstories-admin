function validate(item, lang) {
  const required = ["siteId", "langStoryId", "storyId", "lang", "title", "subtitle", "excerpt", "paragraphs", "moralOrLesson"];
  for (const field of required) {
    if (!item[field]) throw new Error(`${lang}: Missing field "${field}"`);
  }
  if (!Array.isArray(item.paragraphs) || item.paragraphs.length === 0) {
    throw new Error(`${lang}: paragraphs must be a non-empty array`);
  }
  for (let i = 0; i < item.paragraphs.length; i++) {
    if (!item.paragraphs[i].text) {
      throw new Error(`${lang}: paragraph ${i} has no text`);
    }
  }
  const json = JSON.stringify(item);
  const parsed = JSON.parse(json);
  if (parsed.title !== item.title) {
    throw new Error(`${lang}: JSON roundtrip failed for title`);
  }
  const totalChars = item.paragraphs.reduce((s, p) => s + p.text.length, 0);
  console.log(`  OK: ${json.length} bytes JSON, ${item.paragraphs.length} paragraphs, ${totalChars} chars`);
}

