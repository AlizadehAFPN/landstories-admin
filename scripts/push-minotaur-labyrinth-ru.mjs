function validate(item) {
  const json = JSON.stringify(item);
  JSON.parse(json); // round-trip check
  const paraCount = item.paragraphs.length;
  let totalChars = 0;

  console.log(`\n--- Validation: ${item.lang}#${item.storyId} ---`);
  for (let i = 0; i < paraCount; i++) {
    const charCount = item.paragraphs[i].text.length;
    const wordCount = item.paragraphs[i].text.split(/\s+/).length;
    totalChars += charCount;
    console.log(`  P${i + 1}: ${charCount} chars, ${wordCount} words`);
    if (charCount > 500) console.warn(`    \u26A0 OVER 500 chars!`);
    if (wordCount > 100) console.warn(`    \u26A0 OVER 100 words!`);
  }
  console.log(`  Total: ${totalChars} chars, ${paraCount} paragraphs`);
  console.log(`  Target: ~3000 chars (\u00B120% = 2400\u20133600)`);

  if (totalChars < 2400 || totalChars > 3600) {
    console.error("  \u274C Total character count out of range! Aborting.");
    process.exit(1);
  }
  console.log("  \u2705 Validation passed\n");
}

