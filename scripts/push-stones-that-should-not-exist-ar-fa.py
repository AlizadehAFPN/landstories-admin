# ── Push ─────────────────────────────────────────────────────────────
def push_item(item, lang_label):
    try:
        table.put_item(Item=item)
        # Verify
        response = table.get_item(Key={
            "siteId": item["siteId"],
            "langStoryId": item["langStoryId"]
        })
        if "Item" in response:
            stored = response["Item"]
            print(f"[OK] {lang_label} pushed and verified successfully.")
            print(f"     langStoryId: {stored['langStoryId']}")
            print(f"     title: {stored['title']}")
            print(f"     paragraphs: {len(stored['paragraphs'])} paragraphs")
            print(f"     updatedAt: {stored['updatedAt']}")
            return True
        else:
            print(f"[ERROR] {lang_label} push succeeded but verification failed — item not found.")
            return False
    except Exception as e:
        print(f"[ERROR] {lang_label} push failed: {e}")
        return False


