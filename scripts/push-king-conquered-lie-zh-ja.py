def validate_item(item, lang):
    """Validate the item structure before pushing."""
    required = ["siteId", "langStoryId", "lang", "title", "subtitle",
                "excerpt", "paragraphs", "moralOrLesson", "characters",
                "era", "source", "storyId", "storyCategory"]
    for key in required:
        assert key in item, f"[{lang}] Missing key: {key}"

    assert item["lang"]["S"] == lang, f"[{lang}] lang mismatch"
    assert item["langStoryId"]["S"].startswith(f"{lang}#"), f"[{lang}] langStoryId prefix wrong"

    paras = item["paragraphs"]["L"]
    assert 5 <= len(paras) <= 10, f"[{lang}] paragraph count {len(paras)} out of range"

    total_chars = sum(len(p["M"]["text"]["S"]) for p in paras)
    print(f"  [{lang}] {len(paras)} paragraphs, {total_chars} total characters")
    assert 800 <= total_chars <= 2000, f"[{lang}] total chars {total_chars} out of range"

    # Verify JSON round-trip (encoding sanity)
    json_str = json.dumps(item, ensure_ascii=False)
    json.loads(json_str)
    print(f"  [{lang}] JSON round-trip OK ({len(json_str)} bytes)")


# ═══════════════════════════════════════════════════
#  PUSH
# ═══════════════════════════════════════════════════

def push_item(item, lang):
    """Push one item to DynamoDB."""
    validate_item(item, lang)
    print(f"\n  [{lang}] Pushing to DynamoDB...")
    try:
        dynamodb.put_item(TableName=TABLE, Item=item)
        print(f"  [{lang}] SUCCESS")
    except Exception as e:
        print(f"  [{lang}] FAILED: {e}", file=sys.stderr)
        sys.exit(1)

    # Verify by reading back
    resp = dynamodb.get_item(
        TableName=TABLE,
        Key={"siteId": {"S": "persepolis"}, "langStoryId": item["langStoryId"]},
        ProjectionExpression="title, lang, langStoryId, updatedAt"
    )
    readback = resp.get("Item", {})
    assert readback.get("title", {}).get("S") == item["title"]["S"], f"[{lang}] Readback title mismatch!"
    print(f"  [{lang}] Verified: \"{readback['title']['S']}\" (updatedAt={readback['updatedAt']['N']})")


