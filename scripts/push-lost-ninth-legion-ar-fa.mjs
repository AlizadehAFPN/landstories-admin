// ─── Push ────────────────────────────────────────────────────────────────────

async function push(item, label) {
  try {
    await doc.send(
      new PutCommand({
        TableName: TABLE,
        Item: item,
        ConditionExpression: "attribute_not_exists(langStoryId)",
      })
    );
    console.log(`✓ ${label} pushed successfully (langStoryId: ${item.langStoryId})`);
  } catch (err) {
    if (err.name === "ConditionalCheckFailedException") {
      console.error(`✗ ${label} ALREADY EXISTS — skipping to avoid overwrite.`);
    } else {
      console.error(`✗ ${label} FAILED:`, err.message);
      throw err;
    }
  }
}

