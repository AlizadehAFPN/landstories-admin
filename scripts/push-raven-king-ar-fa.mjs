async function pushItem(item, label) {
  console.log(`\n${"=".repeat(50)}`);
  console.log(`Pushing ${label}...`);
  console.log(`  siteId:      ${item.siteId}`);
  console.log(`  langStoryId: ${item.langStoryId}`);
  console.log(`  title:       ${item.title}`);
  console.log(`  paragraphs:  ${item.paragraphs.length}`);

  // Validate JSON serialization
  const json = JSON.stringify(item);
  JSON.parse(json); // will throw if invalid
  console.log(`  JSON valid:  ✓ (${json.length} bytes)`);

  const cmd = new PutCommand({ TableName: TABLE, Item: item });
  const result = await docClient.send(cmd);
  console.log(
    `  HTTP status: ${result.$metadata.httpStatusCode}`
  );
  if (result.$metadata.httpStatusCode === 200) {
    console.log(`  ✅ ${label} pushed successfully!`);
  } else {
    throw new Error(`Unexpected status: ${result.$metadata.httpStatusCode}`);
  }
}

