// ─── Push ───
async function push(item, label) {
  try {
    await docClient.send(
      new PutCommand({
        TableName: "Story",
        Item: item,
        ConditionExpression:
          "attribute_not_exists(siteId) AND attribute_not_exists(langStoryId)",
      })
    );
    console.log(`✓ ${label} pushed successfully (${item.langStoryId})`);
  } catch (err) {
    if (err.name === "ConditionalCheckFailedException") {
      console.error(
        `✗ ${label} ALREADY EXISTS — skipping to avoid overwrite (${item.langStoryId})`
      );
    } else {
      console.error(`✗ ${label} FAILED:`, err.message);
      process.exit(1);
    }
  }
}

// Validate paragraph constraints before pushing
function validate(item, label) {
  let totalChars = 0;
  for (let i = 0; i < item.paragraphs.length; i++) {
    const p = item.paragraphs[i].text;
    const chars = p.length;
    const words = p.split(/\s+/).length;
    totalChars += chars;
    if (chars > 500) {
      console.warn(
        `⚠ ${label} para ${i + 1}: ${chars} chars (max 500)`
      );
    }
    if (words > 100) {
      console.warn(
        `⚠ ${label} para ${i + 1}: ${words} words (max 100)`
      );
    }
  }
  console.log(
    `  ${label}: ${item.paragraphs.length} paragraphs, ~${totalChars} total chars`
  );
}

(async () => {
  console.log("── Validating ──");
  validate(ar, "Arabic");
  validate(fa, "Persian");

  console.log("\n── Pushing to DynamoDB ──");
  await push(ar, "Arabic");
  await push(fa, "Persian");

  console.log("\n── Done ──");
})();
