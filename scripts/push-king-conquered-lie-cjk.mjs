async function pushStory(item) {
  const label = `${item.lang} — ${item.title}`;
  console.log(`\nPushing ${label} ...`);

  // Validate critical fields
  if (!item.siteId || !item.langStoryId || !item.lang || !item.title) {
    throw new Error(`Missing critical field in ${label}`);
  }
  if (!item.paragraphs || item.paragraphs.length < 6) {
    throw new Error(`Too few paragraphs in ${label}: ${item.paragraphs?.length}`);
  }

  // Validate no undefined or null text in paragraphs
  for (let i = 0; i < item.paragraphs.length; i++) {
    if (!item.paragraphs[i].text) {
      throw new Error(`Empty paragraph text at index ${i} in ${label}`);
    }
  }

  const cmd = new PutCommand({
    TableName: TABLE,
    Item: item,
    ConditionExpression: "attribute_not_exists(siteId) AND attribute_not_exists(langStoryId)",
  });

  try {
    await docClient.send(cmd);
    console.log(`  SUCCESS: ${label} (${item.paragraphs.length} paragraphs)`);
  } catch (err) {
    if (err.name === "ConditionalCheckFailedException") {
      console.error(`  ALREADY EXISTS: ${label} — skipping to avoid overwrite`);
    } else {
      throw err;
    }
  }
}

