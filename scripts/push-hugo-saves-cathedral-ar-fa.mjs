async function pushItem(label, item) {
  console.log(`\n⏳ Pushing ${label}...`);

  // Validate JSON structure before pushing
  const jsonStr = JSON.stringify(item);
  JSON.parse(jsonStr); // will throw if malformed
  console.log(`  ✅ JSON valid (${jsonStr.length} bytes)`);

  // Count paragraphs and total text characters
  const paras = item.paragraphs.L;
  let totalChars = 0;
  for (let i = 0; i < paras.length; i++) {
    const t = paras[i].M.text.S;
    totalChars += t.length;
    console.log(`  P${i + 1}: ${t.length} chars`);
  }
  console.log(`  📊 ${paras.length} paragraphs, ${totalChars} total chars`);

  const cmd = new PutItemCommand({ TableName: TABLE, Item: item });
  const res = await client.send(cmd);
  console.log(`  ✅ ${label} pushed — HTTP ${res.$metadata.httpStatusCode}`);
  return res;
}

