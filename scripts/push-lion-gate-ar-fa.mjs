// ─── Push ───

async function push(item, label) {
  console.log(`\n⏳ Pushing ${label}…`);
  try {
    await docClient.send(
      new PutCommand({
        TableName: "Story",
        Item: item,
        ConditionExpression: "attribute_not_exists(langStoryId)",
      })
    );
    console.log(`✅ ${label} pushed successfully.`);
  } catch (err) {
    if (err.name === "ConditionalCheckFailedException") {
      console.log(`⚠️  ${label} already exists — overwriting…`);
      await docClient.send(
        new PutCommand({ TableName: "Story", Item: item })
      );
      console.log(`✅ ${label} overwritten successfully.`);
    } else {
      console.error(`❌ ${label} FAILED:`, err);
      throw err;
    }
  }
}

(async () => {
  await push(ar, "Arabic  (ar#lion-gate-sky-fortress)");
  await push(fa, "Persian (fa#lion-gate-sky-fortress)");
  console.log("\n🎉 All done.");
})();
