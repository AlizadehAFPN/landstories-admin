// ─── PUSH ──────────────────────────────────────────────────────────────────────

async function pushItem(item, label) {
  console.log(`\nPushing ${label}...`);
  console.log(`  siteId: ${item.siteId}`);
  console.log(`  langStoryId: ${item.langStoryId}`);
  console.log(`  title: ${item.title}`);
  console.log(`  paragraphs: ${item.paragraphs.length}`);

  // Validate JSON structure
  const json = JSON.stringify(item);
  JSON.parse(json); // throws if invalid
  console.log(`  JSON valid: ✓ (${json.length} bytes)`);

  await docClient.send(
    new PutCommand({
      TableName: TABLE,
      Item: item,
      ConditionExpression: "attribute_not_exists(siteId) AND attribute_not_exists(langStoryId)",
    })
  );
  console.log(`  ✅ ${label} pushed successfully!`);
}

async function main() {
  try {
    await pushItem(arabic, "Arabic (ar)");
    await pushItem(persian, "Persian (fa)");
    console.log("\n🎉 Both languages pushed successfully!");
  } catch (err) {
    if (err.name === "ConditionalCheckFailedException") {
      console.error(`\n⚠️  Record already exists. Retrying with overwrite...`);
      // Retry without condition
      try {
        await docClient.send(new PutCommand({ TableName: TABLE, Item: arabic }));
        console.log("  ✅ Arabic pushed (overwrite).");
        await docClient.send(new PutCommand({ TableName: TABLE, Item: persian }));
        console.log("  ✅ Persian pushed (overwrite).");
        console.log("\n🎉 Both languages pushed successfully (overwrite)!");
      } catch (retryErr) {
        console.error("Retry failed:", retryErr);
        process.exit(1);
      }
    } else {
      console.error("Error:", err);
      process.exit(1);
    }
  }
}

main();
