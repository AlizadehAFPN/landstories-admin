async function push(item, label) {
  try {
    await client.send(
      new PutItemCommand({
        TableName: TABLE,
        Item: item,
        ConditionExpression:
          "attribute_not_exists(siteId) AND attribute_not_exists(langStoryId)",
      })
    );
    console.log(`✅ ${label} pushed successfully`);
  } catch (err) {
    if (err.name === "ConditionalCheckFailedException") {
      console.error(
        `❌ ${label} already exists! Skipping to prevent overwrite.`
      );
    } else {
      console.error(`❌ ${label} FAILED:`, err.message);
      throw err;
    }
  }
}

