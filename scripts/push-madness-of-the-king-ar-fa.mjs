async function push(item) {
  const label = `${item.lang}#${item.storyId}`;
  console.log(`\n⏳ Pushing ${label} …`);

  await docClient.send(new PutCommand({ TableName: TABLE, Item: item }));
  console.log(`✅ PUT succeeded for ${label}`);

  // verify
  const { Item: saved } = await docClient.send(
    new GetCommand({
      TableName: TABLE,
      Key: { siteId: item.siteId, langStoryId: item.langStoryId },
    }),
  );
  if (!saved) throw new Error(`Verification FAILED for ${label} — item not found`);
  if (saved.title !== item.title) throw new Error(`Verification FAILED for ${label} — title mismatch`);
  console.log(`✅ Verified ${label}: "${saved.title}" (${saved.paragraphs.length} paragraphs)`);
}

(async () => {
  try {
    await push(ar);
    await push(fa);
    console.log('\n🎉 All done — both ar and fa pushed and verified.');
  } catch (err) {
    console.error('\n❌ ERROR:', err);
    process.exit(1);
  }
})();
