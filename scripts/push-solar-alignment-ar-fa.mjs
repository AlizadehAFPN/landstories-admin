// ─── Push ────────────────────────────────────────────────────────────────────

async function push(item) {
  const label = `${item.lang} → ${item.langStoryId}`;
  try {
    await doc.send(new PutCommand({ TableName: TABLE, Item: item }));
    console.log(`✓ ${label}  (updatedAt ${item.updatedAt})`);
  } catch (err) {
    console.error(`✗ ${label}`, err);
    process.exitCode = 1;
  }
}

