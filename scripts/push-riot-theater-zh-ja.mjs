async function push(item, label) {
  console.log(`\nPushing ${label}...`);
  try {
    // Validate JSON serialization first
    const serialized = JSON.stringify(item);
    JSON.parse(serialized); // round-trip check
    console.log(`  JSON validation passed (${serialized.length} bytes)`);

    await client.send(
      new PutItemCommand({
        TableName: TABLE,
        Item: item,
      })
    );
    console.log(`  SUCCESS: ${label} pushed to DynamoDB`);
    console.log(`    siteId: ${item.siteId.S}`);
    console.log(`    langStoryId: ${item.langStoryId.S}`);
    console.log(`    title: ${item.title.S}`);
    return true;
  } catch (err) {
    console.error(`  FAILED: ${label}`, err.message);
    return false;
  }
}

