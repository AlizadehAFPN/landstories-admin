async function pushItem(item, label) {
  try {
    // Validate JSON roundtrip first
    const json = JSON.stringify(item);
    JSON.parse(json); // will throw if invalid
    console.log(`✓ ${label}: JSON validation passed (${json.length} bytes)`);

    await client.send(
      new PutItemCommand({ TableName: TABLE, Item: item })
    );
    console.log(`✓ ${label}: Successfully pushed to DynamoDB`);
  } catch (err) {
    console.error(`✗ ${label}: FAILED —`, err.message);
    throw err;
  }
}

