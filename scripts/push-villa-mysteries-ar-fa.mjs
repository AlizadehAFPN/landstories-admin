function validate(label, item) {
  console.log(`\n${"═".repeat(60)}`);
  console.log(`  VALIDATING: ${label}`);
  console.log(`${"═".repeat(60)}`);

  const required = [
    "siteId", "langStoryId", "storyId", "lang",
    "title", "subtitle", "excerpt", "moralOrLesson", "paragraphs",
  ];
  for (const f of required) {
    if (!item[f]) throw new Error(`Missing required field: ${f}`);
  }
  if (!Array.isArray(item.paragraphs) || item.paragraphs.length === 0) {
    throw new Error("paragraphs must be a non-empty array");
  }

  console.log(`  siteId:       ${item.siteId}`);
  console.log(`  langStoryId:  ${item.langStoryId}`);
  console.log(`  title:        ${item.title}`);
  console.log(`  subtitle:     ${item.subtitle}`);
  console.log(`  paragraphs:   ${item.paragraphs.length}`);

  let totalChars = 0;
  for (let i = 0; i < item.paragraphs.length; i++) {
    const t = item.paragraphs[i].text;
    if (!t) throw new Error(`Paragraph ${i} missing text`);
    const chars = t.length;
    const words = t.split(/\s+/).length;
    totalChars += chars;
    const status =
      chars > 500 ? "⚠️  OVER 500!" : words > 100 ? "⚠️  OVER 100w!" : "✓";
    console.log(`  P${i + 1}: ${chars} chars, ~${words} words ${status}`);
  }
  console.log(`  Total chars:  ${totalChars}`);

  if (item.paragraphs.length < 6 || item.paragraphs.length > 10) {
    console.warn(`  ⚠️  Paragraph count ${item.paragraphs.length} outside 6-8 range`);
  }

  // Validate JSON serialization (catches encoding issues)
  JSON.stringify(item);
  console.log(`  JSON valid:   ✓`);
}

async function push(label, item) {
  validate(label, item);

  console.log(`\n  ⏳ Pushing ${label} to DynamoDB …`);
  await docClient.send(
    new PutCommand({ TableName: TABLE, Item: item })
  );
  console.log(`  ✅ ${label} pushed successfully.`);

  // Verify
  console.log(`  🔍 Verifying …`);
  const { Item: rec } = await docClient.send(
    new GetCommand({
      TableName: TABLE,
      Key: { siteId: item.siteId, langStoryId: item.langStoryId },
    })
  );
  if (!rec) throw new Error("Verification FAILED — record not found!");
  if (rec.title !== item.title) throw new Error("Verification FAILED — title mismatch!");
  if (rec.paragraphs.length !== item.paragraphs.length)
    throw new Error("Verification FAILED — paragraph count mismatch!");
  console.log(`  ✅ Verified: ${rec.lang} | "${rec.title}" | ${rec.paragraphs.length} paragraphs`);
}

