// ─── PUSH & VERIFY ──────────────────────────────────────────────────────────

async function push(item, label) {
  console.log(`\nPushing ${label}...`);
  try {
    await docClient.send(
      new PutCommand({ TableName: TABLE, Item: item })
    );
    console.log(`  ${label} pushed successfully.`);
  } catch (err) {
    console.error(`  FAILED to push ${label}:`, err.message);
    throw err;
  }
}

async function verify(siteId, langStoryId, label) {
  const result = await docClient.send(
    new GetCommand({
      TableName: TABLE,
      Key: { siteId, langStoryId },
    })
  );
  if (result.Item) {
    console.log(
      `  ${label} verified: title="${result.Item.title}", paragraphs=${result.Item.paragraphs.length}, lang=${result.Item.lang}`
    );
  } else {
    console.error(`  ${label} verification FAILED: item not found!`);
  }
}

