// ─── Push each language ───
async function pushStory(item, label) {
  console.log(`\n\u23F3 Pushing ${label}...`);
  try {
    await docClient.send(
      new PutCommand({
        TableName: TABLE,
        Item: item,
        ConditionExpression:
          "attribute_not_exists(siteId) AND attribute_not_exists(langStoryId)",
      })
    );
    console.log(`\u2705 ${label} pushed successfully.`);
    console.log(`   siteId: ${item.siteId}`);
    console.log(`   langStoryId: ${item.langStoryId}`);
    console.log(`   paragraphs: ${item.paragraphs.length}`);
  } catch (err) {
    if (err.name === "ConditionalCheckFailedException") {
      console.log(
        `\u26A0\uFE0F  ${label} already exists. Overwriting with updated version...`
      );
      await docClient.send(
        new PutCommand({
          TableName: TABLE,
          Item: item,
        })
      );
      console.log(`\u2705 ${label} overwritten successfully.`);
    } else {
      console.error(`\u274C ${label} FAILED:`, err.message);
      throw err;
    }
  }
}

async function main() {
  console.log("\u2550".repeat(47));
  console.log("  Water Wizards of Petra \u2014 ar / fa push  ");
  console.log("\u2550".repeat(47));
  console.log(`Timestamp: ${now}`);

  // Validate JSON integrity before pushing
  for (const [label, item] of [
    ["Arabic", arabic],
    ["Persian", persian],
  ]) {
    const json = JSON.stringify(item);
    JSON.parse(json); // will throw if malformed
    console.log(`\u2713 ${label} JSON valid (${json.length} bytes)`);
  }

  // Push sequentially to confirm each before moving on
  await pushStory(arabic, "Arabic (ar)");
  await pushStory(persian, "Persian (fa)");

  console.log("\n" + "\u2550".repeat(47));
  console.log("  Both languages pushed successfully!  ");
  console.log("\u2550".repeat(47));
}

main().catch((err) => {
  console.error("\n\uD83D\uDCA5 Fatal error:", err.message);
  process.exit(1);
});
