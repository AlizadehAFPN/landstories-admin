// ─── Push ────────────────────────────────────────────────────────────────────
async function push(item) {
  const label = `${item.lang}#${item.storyId}`;
  try {
    await doc.send(new PutCommand({ TableName: TABLE, Item: item }));
    console.log(`✅  ${label} pushed successfully`);
  } catch (err) {
    console.error(`❌  ${label} FAILED:`, err);
    process.exitCode = 1;
  }
}

