// PUSH FUNCTION
// ─────────────────────────────────────────────
async function pushStory(item, langLabel) {
  try {
    await client.send(
      new PutItemCommand({
        TableName: TABLE,
        Item: item,
        ConditionExpression:
          "attribute_not_exists(siteId) AND attribute_not_exists(langStoryId)",
      })
    );
    console.log(`[${langLabel}] Successfully pushed: ${item.langStoryId.S}`);
  } catch (err) {
    if (err.name === "ConditionalCheckFailedException") {
      // Record already exists — overwrite it
      console.log(
        `[${langLabel}] Record exists, overwriting: ${item.langStoryId.S}`
      );
      await client.send(
        new PutItemCommand({
          TableName: TABLE,
          Item: item,
        })
      );
      console.log(`[${langLabel}] Successfully overwrote: ${item.langStoryId.S}`);
    } else {
      console.error(`[${langLabel}] FAILED:`, err.message);
      throw err;
    }
  }
}

