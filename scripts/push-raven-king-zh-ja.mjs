// ─── Push ───
async function push(item, label) {
  try {
    await docClient.send(new PutCommand({ TableName: TABLE, Item: item }));
    console.log(`✓ ${label} pushed successfully (${item.langStoryId})`);
  } catch (err) {
    console.error(`✗ ${label} FAILED:`, err);
    process.exit(1);
  }
}

