async function pushStory(item, langLabel) {
  try {
    await docClient.send(
      new PutCommand({
        TableName: "Story",
        Item: item,
        ConditionExpression:
          "attribute_not_exists(siteId) AND attribute_not_exists(langStoryId)",
      })
    );
    console.log(`[${langLabel}] Successfully pushed: ${item.langStoryId}`);
  } catch (err) {
    if (err.name === "ConditionalCheckFailedException") {
      // Record exists, overwrite it
      await docClient.send(
        new PutCommand({
          TableName: "Story",
          Item: item,
        })
      );
      console.log(
        `[${langLabel}] Updated existing record: ${item.langStoryId}`
      );
    } else {
      console.error(`[${langLabel}] FAILED:`, err.message);
      throw err;
    }
  }
}

// Validate paragraph constraints before pushing
function validateStory(item, langLabel) {
  let totalChars = 0;
  for (let i = 0; i < item.paragraphs.length; i++) {
    const text = item.paragraphs[i].text;
    const charCount = text.length;
    const wordCount = text.split(/\s+/).length;
    totalChars += charCount;

    if (charCount > 500) {
      console.error(
        `[${langLabel}] Paragraph ${i + 1} exceeds 500 chars: ${charCount}`
      );
      return false;
    }
    if (wordCount > 100) {
      console.error(
        `[${langLabel}] Paragraph ${i + 1} exceeds 100 words: ${wordCount}`
      );
      return false;
    }
    console.log(
      `  P${i + 1}: ${charCount} chars, ${wordCount} words`
    );
  }
  console.log(
    `[${langLabel}] Total: ${totalChars} chars, ${item.paragraphs.length} paragraphs`
  );
  return true;
}

async function main() {
  console.log("=== Validating Arabic ===");
  if (!validateStory(arItem, "AR")) {
    process.exit(1);
  }

  console.log("\n=== Validating Persian ===");
  if (!validateStory(faItem, "FA")) {
    process.exit(1);
  }

  console.log("\n=== Pushing to DynamoDB ===");
  await pushStory(arItem, "AR");
  await pushStory(faItem, "FA");

  console.log("\nDone! Both stories pushed successfully.");
}

main().catch((err) => {
  console.error("Fatal error:", err);
  process.exit(1);
});
