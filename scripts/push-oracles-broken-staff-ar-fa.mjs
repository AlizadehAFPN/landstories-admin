// ─── Push ───
async function pushStory(item, label) {
  try {
    await docClient.send(
      new PutCommand({
        TableName: TABLE,
        Item: item,
        ConditionExpression:
          "attribute_not_exists(siteId) AND attribute_not_exists(langStoryId)",
      })
    );
    console.log(`\u2713 ${label} pushed successfully (${item.langStoryId})`);
    return true;
  } catch (err) {
    if (err.name === "ConditionalCheckFailedException") {
      console.log(`  ${label} already exists, overwriting...`);
      await docClient.send(new PutCommand({ TableName: TABLE, Item: item }));
      console.log(
        `\u2713 ${label} overwritten successfully (${item.langStoryId})`
      );
      return true;
    }
    console.error(`\u2717 ${label} FAILED:`, err.message);
    return false;
  }
}

async function main() {
  console.log(
    `Pushing 2 stories to table "${TABLE}" at ${new Date().toISOString()}...\n`
  );

  const arOk = await pushStory(arabic, "Arabic (ar)");
  const faOk = await pushStory(persian, "Persian (fa)");

  console.log("\n\u2500\u2500\u2500 Summary \u2500\u2500\u2500");
  console.log(`Arabic:  ${arOk ? "\u2713" : "\u2717"}`);
  console.log(`Persian: ${faOk ? "\u2713" : "\u2717"}`);

  if (!arOk || !faOk) process.exit(1);
}

main();
