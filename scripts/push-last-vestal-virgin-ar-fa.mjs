// ─── Push function ───
async function pushStory(item) {
  const cmd = new PutCommand({ TableName: TABLE, Item: item });
  const res = await docClient.send(cmd);
  console.log(
    `✓ Pushed ${item.lang}#${item.storyId} — HTTP ${res.$metadata.httpStatusCode}`
  );
  return res;
}

// ─── Validate before pushing ───
function validate(item) {
  const errors = [];
  if (!item.siteId) errors.push("missing siteId");
  if (!item.langStoryId) errors.push("missing langStoryId");
  if (!item.lang) errors.push("missing lang");
  if (!item.title) errors.push("missing title");
  if (!item.subtitle) errors.push("missing subtitle");
  if (!item.excerpt) errors.push("missing excerpt");
  if (!item.moralOrLesson) errors.push("missing moralOrLesson");
  if (!item.paragraphs || item.paragraphs.length < 6)
    errors.push("too few paragraphs");
  if (item.paragraphs) {
    item.paragraphs.forEach((p, i) => {
      if (!p.text) errors.push(`paragraph ${i} missing text`);
      if (p.text && p.text.length > 600)
        errors.push(`paragraph ${i} too long: ${p.text.length} chars`);
    });
  }
  // Check total character count
  const totalChars = item.paragraphs.reduce((s, p) => s + p.text.length, 0);
  console.log(
    `  ${item.lang}: ${item.paragraphs.length} paragraphs, ${totalChars} total chars`
  );
  if (errors.length) {
    throw new Error(`Validation failed for ${item.lang}: ${errors.join(", ")}`);
  }
}

// ─── Main ───
async function main() {
  console.log("Validating...\n");
  validate(arabic);
  validate(persian);

  console.log("\nPushing Arabic...");
  await pushStory(arabic);

  console.log("Pushing Persian...");
  await pushStory(persian);

  console.log("\nDone. Both records pushed successfully.");
}

main().catch((err) => {
  console.error("FAILED:", err);
  process.exit(1);
});
