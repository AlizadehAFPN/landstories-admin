function validate(label, item) {
  // Round-trip JSON check
  const json = JSON.stringify(item);
  JSON.parse(json);

  const paraCount = item.paragraphs.length;
  const totalChars = item.paragraphs.reduce((sum, p) => sum + p.text.length, 0);

  console.log(
    `  [VALID] ${label}: ${json.length} bytes, ${paraCount} paragraphs, ~${totalChars} chars of story text`
  );

  // CJK soft limit: ~250 chars per paragraph (adjusted from Latin)
  for (let i = 0; i < paraCount; i++) {
    const len = item.paragraphs[i].text.length;
    if (len > 350) {
      console.warn(`    NOTE: P${i + 1} is ${len} chars (soft limit 300 for CJK)`);
    }
  }

  // Total character count check
  if (totalChars < 800) {
    console.warn(`    WARNING: Total ${totalChars} chars — below 1000 target`);
  }
  if (totalChars > 1800) {
    console.warn(`    WARNING: Total ${totalChars} chars — above 1500+20% target`);
  }
}

async function pushItem(label, item) {
  const cmd = new PutCommand({ TableName: TABLE, Item: item });
  const res = await docClient.send(cmd);
  console.log(
    `  [OK] ${label} pushed (HTTP ${res.$metadata.httpStatusCode})`
  );
}

