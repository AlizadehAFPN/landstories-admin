// ── Validation ──────────────────────────────────────────────
function validate(label, paragraphs) {
  let totalChars = 0;
  let hasError = false;
  console.log(`\n── ${label} ──`);
  for (let i = 0; i < paragraphs.length; i++) {
    const t = paragraphs[i].text;
    const words = t.split(/\s+/).length;
    const chars = t.length;
    totalChars += chars;
    console.log(`  P${i + 1}: ${chars} chars, ${words} words`);
    if (chars > 600) {
      console.error(`  ⚠️  P${i + 1} exceeds 600 char limit (RTL + diacritics allowance)!`);
      hasError = true;
    }
    if (words > 100) {
      console.error(`  ⚠️  P${i + 1} exceeds 100 word limit!`);
      hasError = true;
    }
  }
  console.log(`  Total: ${totalChars} chars (target ~3000 ±20% = 2400–3600)`);
  console.log(`  Paragraphs: ${paragraphs.length} (target 6–8)`);

  if (totalChars < 2000 || totalChars > 4200) {
    console.error(`  ⚠️  Total char count out of range!`);
    hasError = true;
  }
  return hasError;
}

