async function pushStory(item) {
  const label = `${item.lang} (${item.title})`;
  console.log(`\n⏳ Pushing ${label}...`);

  try {
    await docClient.send(
      new PutCommand({
        TableName: TABLE_NAME,
        Item: item,
        ConditionExpression:
          "attribute_not_exists(siteId) AND attribute_not_exists(langStoryId)",
      })
    );
    console.log(`✅ ${label} — pushed successfully.`);
    return true;
  } catch (err) {
    if (err.name === "ConditionalCheckFailedException") {
      // Record already exists — overwrite with a regular put
      console.log(`⚠️  ${label} already exists. Overwriting...`);
      await docClient.send(
        new PutCommand({ TableName: TABLE_NAME, Item: item })
      );
      console.log(`✅ ${label} — overwritten successfully.`);
      return true;
    }
    console.error(`❌ ${label} — FAILED:`, err.message);
    return false;
  }
}

async function main() {
  console.log("═══════════════════════════════════════════════════");
  console.log("  Cloud Maidens — CJK Push (zh, ja, ko)");
  console.log("═══════════════════════════════════════════════════");
  console.log(`Timestamp: ${NOW}`);

  const results = [];

  // Push sequentially so we can confirm each before moving on
  for (const item of [zh, ja, ko]) {
    const ok = await pushStory(item);
    results.push({ lang: item.lang, title: item.title, ok });
  }

  console.log("\n═══════════════════════════════════════════════════");
  console.log("  RESULTS");
  console.log("═══════════════════════════════════════════════════");
  for (const r of results) {
    console.log(`  ${r.ok ? "✅" : "❌"} ${r.lang} — ${r.title}`);
  }

  const allOk = results.every((r) => r.ok);
  if (!allOk) {
    console.error("\n⚠️  Some pushes failed. See above for details.");
    process.exit(1);
  }
  console.log("\n🎉 All three stories pushed successfully.");
}

main();
