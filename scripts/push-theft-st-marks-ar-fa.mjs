// ─── Push ───
async function push(label, item) {
  try {
    await client.send(new PutItemCommand({ TableName: TABLE, Item: item }));
    console.log(`[OK] ${label} pushed successfully.`);
  } catch (err) {
    console.error(`[FAIL] ${label}:`, err.message);
    throw err;
  }
}

// Validate JSON integrity before pushing
function validate(label, item) {
  const json = JSON.stringify(item);
  JSON.parse(json); // will throw if malformed
  const paras = item.paragraphs.L;
  console.log(`[VALIDATE] ${label}: ${paras.length} paragraphs, JSON size=${json.length} bytes`);
  for (let i = 0; i < paras.length; i++) {
    const text = paras[i].M.text.S;
    console.log(`  para ${i+1}: ${text.length} chars`);
    if (text.length > 500) {
      throw new Error(`Para ${i+1} exceeds 500 chars (${text.length})`);
    }
  }
}

