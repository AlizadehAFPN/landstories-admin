// ─── Validation ───
function validate(item, label) {
  let totalChars = 0;
  let pass = true;
  for (let i = 0; i < item.paragraphs.length; i++) {
    const p = item.paragraphs[i].text;
    const chars = p.length;
    const words = p.split(/\s+/).length;
    totalChars += chars;
    if (chars > 500) {
      console.warn(`  \u26a0 ${label} para ${i + 1}: ${chars} chars (max 500)`);
      pass = false;
    }
    if (words > 100) {
      console.warn(`  \u26a0 ${label} para ${i + 1}: ${words} words (max 100)`);
      pass = false;
    }
    console.log(`  Para ${i + 1}: ${chars} chars, ${words} words`);
  }
  console.log(
    `  ${label} total: ${item.paragraphs.length} paragraphs, ${totalChars} chars`
  );
  return pass;
}

// ─── Push ───
async function push(item, label) {
  try {
    await docClient.send(
      new PutCommand({
        TableName: "Story",
        Item: item,
        ConditionExpression:
          "attribute_not_exists(siteId) AND attribute_not_exists(langStoryId)",
      })
    );
    console.log(`\u2713 ${label} pushed successfully (${item.langStoryId})`);
    return true;
  } catch (err) {
    if (err.name === "ConditionalCheckFailedException") {
      console.error(
        `\u2717 ${label} ALREADY EXISTS — skipping (${item.langStoryId})`
      );
      return false;
    }
    console.error(`\u2717 ${label} FAILED:`, err.message);
    throw err;
  }
}

